export const getHardBossDetail = (id: number) => {
    const boss = hardBossMap.get(id)
    if (!boss) return
    const detail: HardBossDetail = {
        ...boss,
        diff: boss.diff.map(item => ({
            ...item,
            dr: mergeDynamicRewards(Object.values(dynamicRewardMap[item.r])),
        })),
    }
    return detail
}

function mergeDynamicRewards(rewards: DynamicReward[]): DynamicReward[] {
    if (rewards.length === 0) return []

    const sortedRewards = [...rewards].sort((a, b) => a.Index - b.Index)
    const merged: DynamicReward[] = []
    let current = { ...sortedRewards[0] }

    for (let i = 1; i < sortedRewards.length; i++) {
        const next = sortedRewards[i]
        if (next.RewardId === current.RewardId) {
            current.EndTime = next.EndTime
        } else {
            merged.push(current)
            current = { ...next }
        }
    }
    merged.push(current)

    return merged
}
