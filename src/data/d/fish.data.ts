export interface Fish {
    id: number
    name: string
    level: number // 等级 直接显示为Lv. 几
    type: number // 1 普通鱼 3 稀有鱼
    rarity: number // 稀有度 显示为颜色区别 2绿 3蓝 4紫 5金
    length: number[] // 长度范围
    icon: string // 对应/imgs/webp/T_Fish_${icon}.webp
    price: number[] // 价格 只取第一个值
    appear: number[] // 出现时间 1=上午 2=下午 3=夜晚
    s2b?: number // 授渔以鱼id
    var?: number[] // 变异id
    varProb?: number // 变异概率
}

export interface FishingSpot {
    id: number
    name: string
    icon: string
    waterType: number // 不显示
    fishCountLimit: number // 鱼数量限制
    replenishDay: number // 不显示
    fishIds: number[] // 可钓鱼id
    showFishIds: number[] // 不显示
    weights: number[] // 鱼权重
    extraReward?: number // 额外奖励id
    extraRewardProb?: number // 额外奖励概率
    petId?: number // 魔灵id
    petProb?: number // 魔灵概率
}

