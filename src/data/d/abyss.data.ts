export interface AbyssBuff {
    id: number
    icon: string
    n: string
    d: string
    t: number
    tip?: string
}
/**
 * 深渊地牢
 */
export interface AbyssDungeon {
    /**
     * 深渊地牢ID
     * 对应 AbyssDungeonId
     */
    id: number
    /**
     * 副本ID
     * 对应 DungeonId
     */
    did: number
    /**
     * 深渊Buff ID列表
     * 对应 AbyssBuffID
     */
    b: number[]
    /**
     * 副本怪物ID列表
     * 对应 DungeonMonsters
     */
    m: number[]
    /**
     * 怪物Buff属性字典
     * 键为元素类型（火/水/风/雷/暗/光），值为属性值
     * 当数据中包含 MonsterBuff.BuffList 时存在
     */
    mb?: {
        [key: string]: number
    }
    /**
     * 赛季ID
     * 对应 AbyssSeason.AbyssSeasonId
     * 当能关联到赛季信息时存在
     */
    sid?: number
    /**
     * 深渊开始时间（Unix时间戳，秒）
     * 对应 AbyssSeason.AbyssStartTime
     */
    st?: number
    /**
     * 深渊结束时间（Unix时间戳，秒）
     * 对应 AbyssSeason.AbyssEndTime
     */
    et?: number
    /**
     * 深渊奖励列表
     * 对应 AbyssSeason.AbyssRewardList → 查询 AbyssRewardList 表
     */
    arl?: Array<{
        /**
         * 等级
         * 对应 AbyssRewardList.Level
         */
        lv: number
        /**
         * 奖励ID
         * 对应 AbyssRewardList.Reward
         */
        r: number
        /**
         * 额外奖励
         * 对应 AbyssRewardList.RewardAddOn（可选）
         */
        a?: number
        /**
         * 是否为密函奖励
         * 对应 AbyssRewardList.WalnutReward（可选）
         */
        w?: 1
    }>
    /**
     * 奖励标题翻译文本
     * 对应 AbyssSeason.AbyssRewardListTitle 的翻译
     */
    art?: string
    /**
     * 赛季名称翻译文本
     * 对应 AbyssSeasonList.AbyssSeasonName 的翻译
     */
    sn?: string
    /**
     * 关联角色ID
     * 对应 AbyssSeasonList.CharId
     * 当能关联到赛季信息时存在
     */
    cid?: number
    /**
     * 深渊Buff列表 (后处理时添加, 与 b 对应)
     * 对应 AbyssBuff
     */
    buff?: AbyssBuff[]
    /**
     * 深渊关联角色名称 cid->charMap[id].名称
     */
    cname?: string
}

